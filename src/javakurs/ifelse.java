package javakurs;

public class ifelse {
   public static void main(String[] args) {
	   
	   int number = -10;
	   if(number<20) {
	       System.out.println("Number is small than 20");
	   }
	   else if(number==20) {
		   System.out.println("Number is equal to 20");
	   }
	   else{
		   System.out.println("Number is big than 20");
	   }
   }
}
