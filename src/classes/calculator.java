package classes;

public class calculator {
  public int sum(int num1,int num2) 
  {
	  return num1+num2;
  }
  public int subraction(int num1,int num2) 
  {
	  return num1-num2;
  }
  public int multiplication(int num1,int num2) 
  {
	  return num1*num2;
  }
  public int division(int num1,int num2) 
  {
		  return num1/num2;
  }
}
